import { useState } from 'react';
import { aiInsights } from '@/mocks/dashboard';
import { useSales } from '@/hooks/useSales';
import { useExpenses } from '@/hooks/useExpenses';
import { useInventory } from '@/hooks/useInventory';
import { useLeads } from '@/hooks/useLeads';
import { useTransactions } from '@/hooks/useTransactions';
import { supabase } from '@/services/supabase';

function parseNum(t: string | number) {
  return typeof t === 'number' ? t : parseFloat(String(t).replace(/[^0-9.]/g, '')) || 0;
}

function ReportModal({ report, onClose }: { report: string; onClose: () => void }) {
  const sections = report.split(/^##\s+/m).filter(Boolean);

  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center p-4" style={{ background: 'rgba(7,16,31,0.6)', backdropFilter: 'blur(4px)' }} onClick={onClose}>
      <div className="w-full max-w-2xl bg-white rounded-2xl overflow-hidden shadow-2xl" onClick={e => e.stopPropagation()}>
        {/* Header */}
        <div className="flex items-center justify-between px-6 py-4 border-b border-slate-100" style={{ background: 'linear-gradient(135deg, #07101F, #0D1F4A)' }}>
          <div className="flex items-center gap-2.5">
            <div className="w-8 h-8 rounded-xl flex items-center justify-center" style={{ background: 'linear-gradient(135deg, #F5A623, #D4890A)' }}>
              <i className="ri-sparkling-2-fill text-white text-sm" />
            </div>
            <div>
              <p className="text-sm font-bold text-white">AI Business Report</p>
              <p className="text-[10px] text-white/40">Generated by iDeals AI · {new Date().toLocaleDateString('en-GH', { day: 'numeric', month: 'short', year: 'numeric' })}</p>
            </div>
          </div>
          <button onClick={onClose} className="w-8 h-8 flex items-center justify-center rounded-lg cursor-pointer" style={{ background: 'rgba(255,255,255,0.1)' }}>
            <i className="ri-close-line text-white text-sm" />
          </button>
        </div>

        {/* Content */}
        <div className="px-6 py-5 max-h-[70vh] overflow-y-auto space-y-4">
          {sections.length > 0 ? sections.map((section, i) => {
            const lines = section.split('\n').filter(Boolean);
            const title = lines[0];
            const body  = lines.slice(1);
            return (
              <div key={i}>
                <h4 className="text-sm font-bold text-slate-800 mb-2 flex items-center gap-2">
                  <span className="w-1.5 h-1.5 rounded-full bg-amber-400 flex-shrink-0" />
                  {title}
                </h4>
                <ul className="space-y-1.5 pl-4">
                  {body.map((line, j) => {
                    const clean = line.replace(/^[-*•]\s*/, '').replace(/\*\*(.*?)\*\*/g, '$1');
                    if (!clean.trim()) return null;
                    return (
                      <li key={j} className="text-xs text-slate-600 leading-relaxed flex items-start gap-2">
                        <span className="text-slate-300 mt-1 flex-shrink-0">›</span>
                        <span>{clean}</span>
                      </li>
                    );
                  })}
                </ul>
              </div>
            );
          }) : (
            <p className="text-sm text-slate-600 whitespace-pre-wrap leading-relaxed">{report}</p>
          )}
        </div>

        {/* Footer */}
        <div className="px-6 py-4 border-t border-slate-100 flex justify-end gap-3">
          <button
            onClick={() => {
              const blob = new Blob([report], { type: 'text/plain' });
              const url = URL.createObjectURL(blob);
              const a = document.createElement('a');
              a.href = url; a.download = `ideals-report-${Date.now()}.txt`; a.click();
              URL.revokeObjectURL(url);
            }}
            className="px-4 py-2 rounded-xl text-xs font-semibold text-slate-600 border border-slate-200 hover:bg-slate-50 cursor-pointer flex items-center gap-1.5"
          >
            <i className="ri-download-line" />Download
          </button>
          <button onClick={onClose} className="px-4 py-2 rounded-xl text-xs font-semibold text-white cursor-pointer" style={{ background: '#0D1F4A' }}>
            Close
          </button>
        </div>
      </div>
    </div>
  );
}

export default function AIPanel() {
  const [expanded, setExpanded] = useState<number | null>(null);
  const [generating, setGenerating] = useState(false);
  const [report, setReport] = useState<string | null>(null);
  const [error, setError] = useState<string | null>(null);

  const { sales } = useSales();
  const { expenses } = useExpenses();
  const { products } = useInventory();
  const { leads } = useLeads();
  const { transactions } = useTransactions();

  async function handleGenerateReport() {
    setGenerating(true);
    setError(null);

    const now = new Date();
    const thisMonth = now.toLocaleDateString('en-GH', { month: 'short' });
    const thisYear  = String(now.getFullYear());

    const activeSales = sales.filter(s => s.status !== 'cancelled' && s.status !== 'refunded');
    const monthSales  = activeSales.filter(s => s.date?.includes(thisMonth) && s.date?.includes(thisYear));

    function estimateCogs(itemsStr: string) {
      return itemsStr.split(',').reduce((sum, part) => {
        const m = part.trim().match(/^(\d+)x\s+(.+)$/);
        if (!m) return sum;
        const p = products.find(p => p.name.toLowerCase() === m[2].trim().toLowerCase());
        return sum + (p?.costPrice ? p.costPrice * parseInt(m[1]) : 0);
      }, 0);
    }

    const revenue   = monthSales.reduce((s, x) => s + parseNum(x.total), 0);
    const cogs      = monthSales.reduce((s, x) => s + (x.cogs ?? estimateCogs(x.items)), 0);
    const opex      = expenses.filter(e => e.type === 'expense' && e.date?.includes(thisMonth) && e.date?.includes(thisYear)).reduce((s, e) => s + parseNum(e.amount), 0);
    const gross     = revenue - cogs;
    const net       = gross - opex;
    const margin    = revenue > 0 ? Math.round((net / revenue) * 100) : 0;

    const prevDate   = new Date(now.getFullYear(), now.getMonth() - 1, 1);
    const prevMonth  = prevDate.toLocaleDateString('en-GH', { month: 'short' });
    const prevYear   = String(prevDate.getFullYear());
    const prevSales  = activeSales.filter(s => s.date?.includes(prevMonth) && s.date?.includes(prevYear));
    const prevRev    = prevSales.reduce((s, x) => s + parseNum(x.total), 0);

    const ytdSales   = activeSales.filter(s => s.date?.includes(thisYear));
    const ytdRev     = ytdSales.reduce((s, x) => s + parseNum(x.total), 0);
    const ytdCogs    = ytdSales.reduce((s, x) => s + (x.cogs ?? estimateCogs(x.items)), 0);
    const ytdOpEx    = expenses.filter(e => e.type === 'expense' && e.date?.includes(thisYear)).reduce((s, e) => s + parseNum(e.amount), 0);
    const ytdNet     = ytdRev - ytdCogs - ytdOpEx;

    const topProducts = activeSales
      .flatMap(s => s.items.split(',').map(i => i.trim().replace(/^\d+x\s+/, '')))
      .reduce((acc: Record<string, number>, name) => { acc[name] = (acc[name] ?? 0) + 1; return acc; }, {});
    const topProductsList = Object.entries(topProducts).sort((a, b) => b[1] - a[1]).slice(0, 3).map(([n, c]) => `${n} (${c}x)`).join(', ');

    const pendingAmt = transactions.filter(t => t.status === 'pending' || t.status === 'needs_review').reduce((s, t) => s + parseNum(t.amount), 0);

    try {
      const { data, error: fnErr } = await supabase.functions.invoke('generate-report', {
        body: {
          period: `${thisMonth} ${thisYear}`,
          revenue, prevRevenue: prevRev,
          cogs, opex,
          grossProfit: gross, netProfit: net, netMargin: margin,
          totalSales: monthSales.length,
          voidedSales: sales.filter(s => s.status === 'cancelled' || s.status === 'refunded').length,
          topProducts: topProductsList || 'No sales yet',
          stockAlerts: products.filter(p => p.stock <= 2).length,
          outOfStock: products.filter(p => p.stock === 0).length,
          activeLeads: leads.filter(l => l.status === 'hot' || l.status === 'warm').length,
          hotLeads: leads.filter(l => l.status === 'hot').length,
          pendingPayments: pendingAmt,
          ytdRevenue: ytdRev, ytdNetProfit: ytdNet,
        },
      });

      if (fnErr) throw new Error(fnErr.message);
      setReport(data.report);
    } catch (e) {
      setError('Could not generate report. Check your connection and try again.');
      console.error(e);
    } finally {
      setGenerating(false);
    }
  }

  return (
    <>
      <div className="rounded-2xl p-5 text-white h-full flex flex-col relative overflow-hidden" style={{ background: 'linear-gradient(160deg, #07101F 0%, #0D1F4A 55%, #1552A8 100%)' }}>
        <div className="absolute top-0 right-0 w-32 h-32 rounded-full blur-3xl pointer-events-none" style={{ background: 'rgba(245,166,35,0.1)' }} />
        <div className="absolute bottom-0 left-0 w-24 h-24 rounded-full blur-3xl pointer-events-none" style={{ background: 'rgba(36,99,190,0.2)' }} />

        <div className="relative flex items-center gap-2.5 mb-4">
          <div className="w-8 h-8 flex items-center justify-center rounded-xl flex-shrink-0" style={{ background: 'linear-gradient(135deg, #F5A623, #D4890A)' }}>
            <i className="ri-sparkling-2-fill text-white text-sm" />
          </div>
          <div>
            <h3 className="text-sm font-bold text-white">AI Intelligence</h3>
            <p className="text-[10px] text-white/40">Live insights</p>
          </div>
          <div className="ml-auto flex items-center gap-1.5">
            <div className="w-1.5 h-1.5 rounded-full animate-pulse" style={{ background: '#F5A623' }} />
            <span className="text-[10px] font-medium" style={{ color: '#F5A623' }}>Active</span>
          </div>
        </div>

        <div className="relative flex-1 space-y-2 overflow-y-auto">
          {aiInsights.map((insight, i) => (
            <div key={i} onClick={() => setExpanded(expanded === i ? null : i)} className="group cursor-pointer">
              <div className={`flex items-start gap-2.5 p-3 rounded-xl transition-all ${expanded === i ? 'bg-white/10' : 'hover:bg-white/5'}`}>
                <i className="ri-checkbox-circle-fill text-sm mt-0.5 flex-shrink-0" style={{ color: '#F5A623' }} />
                <p className="text-xs text-white/75 leading-relaxed flex-1">{insight}</p>
                <i className={`ri-arrow-down-s-line text-sm text-white/20 flex-shrink-0 transition-transform ${expanded === i ? 'rotate-180' : ''}`} />
              </div>
            </div>
          ))}
        </div>

        {error && (
          <p className="relative mt-3 text-[11px] text-rose-300 text-center">{error}</p>
        )}

        <div className="relative mt-4 pt-4 border-t border-white/10">
          <button
            onClick={handleGenerateReport}
            disabled={generating}
            className="w-full flex items-center justify-center gap-2 py-2.5 rounded-xl border border-white/10 text-xs font-semibold text-white hover:border-white/20 transition-all cursor-pointer disabled:opacity-60 disabled:cursor-not-allowed whitespace-nowrap"
            style={{ background: 'linear-gradient(135deg, rgba(245,166,35,0.2), rgba(16,185,129,0.12))' }}
          >
            {generating ? (
              <>
                <i className="ri-loader-4-line animate-spin text-xs" />
                Generating Report…
              </>
            ) : (
              <>
                <i className="ri-sparkling-2-line text-xs" />
                Generate Full AI Report
              </>
            )}
          </button>
        </div>
      </div>

      {report && <ReportModal report={report} onClose={() => setReport(null)} />}
    </>
  );
}
